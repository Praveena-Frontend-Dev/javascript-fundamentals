// Purpose: Execute different blocks based on multiple conditions

const studentScore = 72;

if (studentScore >= 90) {
    console.log("Grade: A");
} else if (studentScore >= 75) {
    console.log("Grade: B");
} else if (studentScore >= 60) {
    console.log("Grade: C");
} else {
    console.log("Grade: Fail");
}
