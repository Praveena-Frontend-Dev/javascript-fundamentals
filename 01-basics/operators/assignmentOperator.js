// Purpose: Update values using assignment operators

let baseValue = 10;
let secondValue = 3;

// Assignment
baseValue = secondValue;
console.log("Assignment (=):", baseValue);

// Reset baseValue
baseValue = 10;

// Addition Assignment
baseValue += secondValue;
console.log("Addition Assignment (+=):", baseValue);

// Reset baseValue
baseValue = 10;

// Subtraction Assignment
baseValue -= secondValue;
console.log("Subtraction Assignment (-=):", baseValue);

// Reset baseValue
baseValue = 10;

// Multiplication Assignment
baseValue *= secondValue;
console.log("Multiplication Assignment (*=):", baseValue);

// Reset baseValue
baseValue = 10;

// Division Assignment
baseValue /= secondValue;
console.log("Division Assignment (/=):", baseValue);

// Reset baseValue
baseValue = 10;

// Modulus Assignment
baseValue %= secondValue;
console.log("Modulus Assignment (%=):", baseValue);
