let baseValue = 10;
let secondValue = 3;

// Addition
const additionValue = baseValue + secondValue;
console.log("Addition:", additionValue);

// Subtraction
const subtractionValue = baseValue - secondValue;
console.log("Subtraction:", subtractionValue);

// Multiplication
const multiplicationValue = baseValue * secondValue;
console.log("Multiplication:", multiplicationValue);

// Division
const divisionValue = baseValue / secondValue;
console.log("Division:", divisionValue);

// Modulus (Remainder)
const remainderValue = baseValue % secondValue;
console.log("Modulus:", remainderValue);

// Increment
baseValue++;
console.log("Incremented baseValue:", baseValue);

// Decrement
secondValue--;
console.log("Decremented secondValue:", secondValue);
